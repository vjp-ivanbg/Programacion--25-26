public class Calculadora {
    
    public static void main (String[] args) {
    /**
     * Método que devuelve una suma de 2 números
     * @param num1
     * @param num2
     * @return la suma de los 2 números
     */
    }
    public static int suma (int num1, int num2) {
        return num1 + num2;
    }
    /**
     * Método que resta 2 números
     * @param num1
     * @param num2
     * @return la resta de los numeros
     */
    public static int resta(int num1, int num2) {
        return num1- num2;
    }
    /**
     * Método que multiplica los dos números pasados por parámetro
     * @param num1
     * @param num2
     * @return devuelve el producto
     */
    public static int multiplicacion(int num1, int num2) {
        return num1 * num2;
    }
    
    /**
     * Método que divide dos números
     * @param num1
     * @param num2
     * @return devuelve la division
     */
    public static int division(int num1, int num2){
        return num1 / num2;
    }
    
    /**
     * Método que calcula la raíz cuadrada
     * @param num1
     * @param num2
     * @return devuelve la raíz cuadrada
     */
    public static double raizcuadrada(int num1) {
        return Math.sqrt(num1);
    }
}

